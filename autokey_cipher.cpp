std::string autokey_cipher::encrypt(std::string text, std::string key)
{
	std::string alphabet = "abcdefghijklmnopqrstuvwxyz";
	std::string result = "";
	key += text;
	int p_int = 0;
	int k_int = 0;

	for (size_t i{}; i < text.length(); i++)
	{
		p_int = alphabet.find(tolower(text[i]));
		k_int = alphabet.find(tolower(key[i]));
		result += tabula_recta[p_int][k_int];
	}

	return result;
}

std::string autokey_cipher::decrypt(std::string text, std::string key)
{
	std::string alphabet = "abcdefghijklmnopqrstuvwxyz";
	std::string result = "";
	int p_int = 0;
	int k_int = 0;
	for (size_t i{}; i < text.length(); i++)
	{
		k_int = alphabet.find(tolower(key[i]));
		for (int j = 0; j < 26; j++)
		{
			if (tabula_recta[j][k_int] == text[i])
				p_int = j;
		}
		result += alphabet[p_int];
		key += alphabet[p_int];

	}
	return result;
}
